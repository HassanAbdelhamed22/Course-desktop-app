    private void ed_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ed_buttonActionPerformed
        // TODO add your handling code here:
       
        if (!crs_name_text.getText().isEmpty() && !id_text.getText().isEmpty() ) {
            try {
                String courseName = crs_name_text.getText();
                String hour = hours_box.getSelectedItem().toString();
                int Id = Integer.parseInt(id_text.getText());

                PreparedStatement stmt = con.prepareStatement("update course set course_name=? , hours=? where course.id = ?");
                stmt.setInt(3,Id);
                stmt.setString(1, courseName);
                stmt.setString(2, hour);
                stmt.execute();
                JOptionPane.showMessageDialog(this, "Edited successfully");
                //fillTableModel();
                ManageCourse mc = new ManageCourse();
                mc.show();
                dispose();
                
                
            } catch (SQLException ex) {
                Logger.getLogger(addCourse.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_ed_buttonActionPerformed

