    private void fillTableModel(){
        try{
            PreparedStatement stmt= con.prepareStatement("select course.id,course.course_name,course.hours from course");
            ResultSet rs = stmt.executeQuery(); 
            
            while(rs.next()){
                
                dtm.addRow(new Object[]{rs.getInt(1),rs.getString(2),rs.getInt(3)});
            }
            table_show.setModel(dtm);
        }catch(SQLException ex) {
                Logger.getLogger(ManageCourse.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
