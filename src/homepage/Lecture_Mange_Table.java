    private void BTNADDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNADDActionPerformed

        //THE CODE FOR ADD BUTTON (INSERT DATA TO THE TABLE)
        if (!txtLectureNo.getText().isEmpty() && !txtAssignmentNo.getText().isEmpty()) {
            try {
                int lec_text = Integer.parseInt(txtLectureNo.getText());
                int ass_text = Integer.parseInt(txtAssignmentNo.getText());
                int Id_text = Integer.parseInt(id_text.getText());
                PreparedStatement stmt = con.prepareStatement("insert into lecture(lecture.id,lecture_number,ass_number) values(?,?,?)");
                stmt.setInt(1, Id_text);
                stmt.setInt(2, lec_text);
                stmt.setInt(3, ass_text);
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(this, "Added successfully");
                Lecture_Mange_Table lmt = new Lecture_Mange_Table();
                lmt.show();
                dispose();
            } catch (SQLException ex) {
                Logger.getLogger(Lecture_Mange_Table.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_BTNADDActionPerformed

