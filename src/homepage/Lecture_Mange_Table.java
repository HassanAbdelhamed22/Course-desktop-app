    private void fillTableModel() {
        try {
            PreparedStatement stmt = con.prepareStatement("select course.id,course.course_name,course.hours,lecture.lecture_number,lecture.ass_number from course inner join lecture on course.id = lecture.id");
            ResultSet rs = stmt.executeQuery();

            while (rs.next()) {

                dtm.addRow(new Object[]{rs.getInt(1), rs.getString(2), rs.getInt(3), rs.getInt(4), rs.getInt(5)});
            }
            tbl_show.setModel(dtm);
        } catch (SQLException ex) {
            Logger.getLogger(ManageCourse.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

